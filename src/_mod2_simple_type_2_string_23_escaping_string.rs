pub(crate) fn test() {

    // \t Insert a tab.
    // \n Insert a newline.
    // \r Insert a carriage return.
    // \' or ' Insert a single quote.
    // \" Insert a double quote.
    // \\ Insert a backslash character.

    let str = "She said \"Hello!\" to me.";
    //str is "She said "Hello!" to me."

    println!("str is \"{str}\"");


}